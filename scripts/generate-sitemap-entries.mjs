import { execFileSync } from "node:child_process";
import { mkdirSync, statSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const projectRoot = path.resolve(__dirname, "..");
const generatedOutputPath = path.join(
  projectRoot,
  "app",
  "sitemap.generated.ts",
);

const sharedRouteSourcePaths = [
  "app/layout.tsx",
  "app/globals.css",
  "app/components/Header.tsx",
  "app/components/HeaderMobileMenu.tsx",
  "app/components/HeaderScrollState.tsx",
  "app/components/Footer.tsx",
];

// Intentionally exclude /contact/thank-you because it is a post-submission
// utility page and should not be indexed.
const routeDefinitions = [
  {
    pathname: "/",
    sourcePaths: ["app/page.tsx", ...sharedRouteSourcePaths],
    priority: 1,
    changeFrequency: "monthly",
  },
  {
    pathname: "/about",
    sourcePaths: ["app/about/page.tsx", ...sharedRouteSourcePaths],
    priority: 0.7,
    changeFrequency: "monthly",
  },
  {
    pathname: "/services",
    sourcePaths: ["app/services/page.tsx", ...sharedRouteSourcePaths],
    priority: 0.8,
    changeFrequency: "monthly",
  },
  {
    pathname: "/faqs",
    sourcePaths: ["app/faqs/page.tsx", ...sharedRouteSourcePaths],
    priority: 0.7,
    changeFrequency: "monthly",
  },
  {
    pathname: "/contact",
    sourcePaths: [
      "app/contact/page.tsx",
      "app/components/ContactForm.tsx",
      ...sharedRouteSourcePaths,
    ],
    priority: 0.7,
    changeFrequency: "monthly",
  },
];

function getLastModified(sourcePath) {
  const absoluteSourcePath = path.join(projectRoot, sourcePath);

  try {
    const isDirty = execFileSync(
      "git",
      ["status", "--porcelain", "--", sourcePath],
      {
        cwd: projectRoot,
        encoding: "utf8",
        stdio: ["ignore", "pipe", "ignore"],
      },
    ).trim();

    if (isDirty) {
      return statSync(absoluteSourcePath).mtime.toISOString();
    }

    const gitLastModified = execFileSync(
      "git",
      ["log", "-1", "--format=%cI", "--", sourcePath],
      {
        cwd: projectRoot,
        encoding: "utf8",
        stdio: ["ignore", "pipe", "ignore"],
      },
    ).trim();

    if (gitLastModified) {
      return gitLastModified;
    }
  } catch {
    // Fall through to the source file mtime when git history is unavailable.
  }

  return statSync(absoluteSourcePath).mtime.toISOString();
}

function getLatestLastModified(sourcePaths) {
  return sourcePaths
    .map((sourcePath) => getLastModified(sourcePath))
    .sort((left, right) => Date.parse(right) - Date.parse(left))[0];
}

const sitemapEntries = routeDefinitions.map((route) => ({
  pathname: route.pathname,
  lastModified: getLatestLastModified(route.sourcePaths),
  priority: route.priority,
  changeFrequency: route.changeFrequency,
}));

const fileContents = `export type SitemapGeneratedEntry = {
  pathname: string;
  lastModified: string;
  priority: number;
  changeFrequency: "monthly";
};

// Generated by scripts/generate-sitemap-entries.mjs
export const sitemapGeneratedEntries: SitemapGeneratedEntry[] = ${JSON.stringify(
  sitemapEntries,
  null,
  2,
)};
`;

mkdirSync(path.dirname(generatedOutputPath), { recursive: true });
writeFileSync(generatedOutputPath, fileContents);
